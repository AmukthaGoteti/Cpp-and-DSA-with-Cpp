#include <iostream>
using namespace std;

int main(int argc, const char * argv[]) {
    // Creating a single character variable
    int num = 3;
    // Accessing and printing above variable
    cout << num << endl;
    // Update the value
    num = 7;
    cout << num << endl;
    int Num;
    Num = 3;
    cout << Num << endl;

    int num1 = 10, num2;
    num2 = num1;
    cout << num1 << " = " << num2 << endl;

    cout << 10 + 20 << endl;

    int num3 = 10, num4 = 20;
    cout << num3 + num4 << endl;

    const int num5 = 10;
    cout << num5 << endl;
}
